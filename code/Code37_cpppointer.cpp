#include <iostream>
using namespace std;

int main()
{
	int x = 10;
	int* px = &x;
	int** ppx = &px;

	cout << "변수  x 값 = " << x << " " << *px << " " << **ppx << endl;
	cout << "변수  x  주소 = 0x" << &x << " 0x" << px << " 0x" << *ppx << endl;
	cout << "포인터 px 주소 = 0x" << &px << " 0x" << ppx << endl;
	cout << "포인터 ppx 주소 = 0x" << x << " 0x" << *px << " 0x" << **ppx << endl;
}
