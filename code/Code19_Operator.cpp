#include <iostream>
using namespace std;
int main()
{
	int data1, data2;
	int num;

	cin >> data1;
	cin >> data2;
	cout << endl;

	if (data1 > data2) num = data1;
	else num = data2;

	for (int i = 1; i < num, i++;)
	{
		if ((data1 % i == 0) && (data2 % i == 0))
			cout << i << " ";
		if ((i == data1) || (i == data2))
			break;

	}
}
