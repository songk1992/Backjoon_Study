#include <iostream>
#include <queue>

using namespace std;

int main()
{
	//q 라는 큐 생성
	queue<int>q;

	//push(element) 큐 뒤에 원소 추가
	q.push(1);
	q.push(2);
	q.push(3);
	q.push(4);
	q.push(5);
	q.push(6);

	//pop() 큐 앞에 원소 삭제
	q.pop();
	q.pop();
	q.pop();
	
	//front 큐 제일 앞에 있는 원소 반환
	cout << "front element : " << q.front() << endl;

	//back 큐 제일 뒤에 있는 원소를 반환
	cout << "back element : " << q.back() << endl;

	//size 큐 사이즈를 반환
	cout << "queue size : " << q.size() << endl;

	//empty
	cout << "is it empty?" << (q.empty() ? "Yes" : "NO") << endl;

	return 0;
}
